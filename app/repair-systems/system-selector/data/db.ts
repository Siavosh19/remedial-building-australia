// ── System Assembly Engine ───────────────────────────────────────────────────

type SystemStage = {
  stepLabel: string;
  functionGroup: FunctionGroup;
  categories: Category[];
  note?: string;
};

export function assembleSystem(answers: SelectorAnswers): SystemStage[] {
  const { defect, orientation, depthMm, structural, rebarExposed, crackActive, masonry } = answers;
  if (!defect) return [];

  // Filter categories for this defect
  const candidateCategories = CATEGORIES.filter(cat => {
    if (!cat.defects.includes(defect)) return false;
    if (rebarExposed === false && cat.rebarExposedRequired) return false;
    if (rebarExposed === true && cat.functionGroup === "rebar-primer") return true;
    if (masonry === false && cat.masonryOnly) return false;
    if (cat.functionGroup === "crack-injection") {
      if (crackActive === true && !cat.activeCrackOK) return false;
      if (crackActive === false && cat.activeCrackOK && !cat.structuralOK) return false;
    }
    if (cat.functionGroup === "sealant" && crackActive === false && structural === true) return false;
    if (cat.functionGroup === "structural-anchor" && !structural) return false;
    if (cat.functionGroup === "repointing-mortar" && masonry !== true) return false;
    if (cat.functionGroup === "floor-levelling" && cat.id.includes("self-levelling")) return true;
    if (cat.functionGroup === "floor-patching") return true;
    if (depthMm !== null && cat.depthMinMm !== null && depthMm < cat.depthMinMm) return false;
    if (depthMm !== null && cat.depthMaxMm !== null && depthMm > cat.depthMaxMm && cat.functionGroup === "repair-mortar" && !cat.structuralOK) return false;
    return true;
  });

  // Crack-specific routing:
  // Active crack → PU injection + ports (not epoxy, not sealant)
  // Dormant structural → epoxy + ports
  // Dormant non-structural → sealant + backer
  const isInjectionDefect = defect === "cracking" || defect === "settlement";
  const stages = new Map<string, Category[]>();
  
  for (const cat of candidateCategories) {
    if (isInjectionDefect) {
      const isSealantRoute = crackActive !== false && !structural;
      const isInjectionRoute = crackActive === true || structural === true;
      if (cat.functionGroup === "crack-injection") {
        if (crackActive === true && !cat.activeCrackOK) continue;
        if (crackActive === false && cat.activeCrackOK && cat.functionGroup === "crack-injection" && !cat.structuralOK) continue;
      }
      if (cat.functionGroup === "sealant" && structural === true) continue;
      if (cat.functionGroup === "sealant-accessory" && structural === true) continue;
      if ((cat.functionGroup === "crack-injection" || cat.functionGroup === "injection-accessory") && !structural && crackActive !== true) continue;
    }
    const key = `${cat.applicationStep}-${cat.functionGroup}`;
    if (!stages.has(key)) stages.set(key, []);
    stages.get(key)!.push(cat);
  }

  // Build stage labels
  const groupLabels: Record<FunctionGroup, string> = {
    "surface-prep": "Surface preparation",
    "rebar-primer": "Rebar primer / corrosion inhibitor",
    "bonding-agent": "Bonding agent",
    "corrosion-inhibitor": "Migrating corrosion inhibitor",
    "repair-mortar": "Repair mortar",
    "crack-injection": "Crack injection resin",
    "injection-accessory": "Crack injection ports",
    "sealant": "Joint sealant",
    "sealant-accessory": "Backer rod",
    "structural-anchor": "Structural anchor / stitching",
    "repointing-mortar": "Masonry repointing mortar",
    "curing": "Curing compound",
    "moisture-primer": "Moisture suppression primer",
    "floor-levelling": "Self-levelling underlayment",
    "floor-patching": "Floor patching compound",
  };

  const result: SystemStage[] = [];
  const sortedKeys = Array.from(stages.keys()).sort((a, b) => {
    const aStep = parseInt(a.split("-")[0]);
    const bStep = parseInt(b.split("-")[0]);
    return aStep - bStep;
  });

  for (const key of sortedKeys) {
    const cats = stages.get(key)!;
    const fg = cats[0].functionGroup;
    result.push({
      stepLabel: groupLabels[fg] || fg,
      functionGroup: fg,
      categories: cats,
    });
  }

  return result;
}

// ── Product helpers ──────────────────────────────────────────────────────────

export function getProductsForCategory(categoryId: string): Product[] {
  const prods = PRODUCTS.filter(p => p.categoryId === categoryId);
  // Sponsored first (within technically suitable)
  return [...prods.filter(p => p.sponsored), ...prods.filter(p => !p.sponsored)];
}

export function getCategoryById(id: string): Category | undefined {
  return CATEGORIES.find(c => c.id === id);
}
